f(8)=P_dbm(128-32)/max(P_dbm);
f(9)=P_dbm(128-48)/max(P_dbm);
f(10)=P_dbm(128-64)/max(P_dbm);
f(11)=P_dbm(129+4)/max(P_dbm);
f(12)=P_dbm(129+8)/max(P_dbm);
